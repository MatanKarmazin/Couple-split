export type TranslationKey = keyof typeof translations.en;

export function translate(language: Language, key: TranslationKey, values: Record<string, string | number> = {}) {
  let text: string = translations[language][key] ?? translations.en[key];
  for (const [name, value] of Object.entries(values)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

export function categoryLabel(language: Language, category: Category) {
  return translate(language, `category.${category}` as TranslationKey);
}

export function splitTypeLabel(language: Language, splitType: SplitType) {
  return translate(language, `split.${splitType}` as TranslationKey);
}

export function shortSplitTypeLabel(language: Language, splitType: SplitType) {
  if (splitType === "one_person") return translate(language, "split.onePersonShort");
  if (splitType === "amounts") return translate(language, "split.amountsShort");
  if (splitType === "percentage") return translate(language, "split.percentageShort");
  return translate(language, "split.equalShort");
}

export function localeForLanguage(language: Language) {
  return language === "he" ? "he-IL" : "en-US";
}

export function directionForLanguage(language: Language): "ltr" | "rtl" {
  return language === "he" ? "rtl" : "ltr";
}

export function systemLanguage() {
  if (typeof navigator === "undefined") return "en";
  return navigator.language.toLowerCase().startsWith("he") ? "he" : "en";
}
